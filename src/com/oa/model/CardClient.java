package com.oa.model;

public class CardClient {
	private int id;
	private String client_name;
	private String team;
	private String work;
	private String work_post;
	private String mobile_phone;
	private String work_phone;	
	private String work_fax;
	private String email;
	private String work_address;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getClient_name() {
		return client_name;
	}
	public void setClient_name(String client_name) {
		this.client_name = client_name;
	}
	public String getTeam() {
		return team;
	}
	public void setTeam(String team) {
		this.team = team;
	}
	public String getWork() {
		return work;
	}
	public void setWork(String work) {
		this.work = work;
	}
	public String getWork_post() {
		return work_post;
	}
	public void setWork_post(String work_post) {
		this.work_post = work_post;
	}
	
	public String getMobile_phone() {
		return mobile_phone;
	}
	public void setMobile_phone(String mobile_phone) {
		this.mobile_phone = mobile_phone;
	}
	public String getWork_phone() {
		return work_phone;
	}
	public void setWork_phone(String work_phone) {
		this.work_phone = work_phone;
	}
	public String getWork_fax() {
		return work_fax;
	}
	public void setWork_fax(String work_fax) {
		this.work_fax = work_fax;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getWork_address() {
		return work_address;
	}
	public void setWork_address(String work_address) {
		this.work_address = work_address;
	}

	
}
